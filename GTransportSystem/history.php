<p><strong>An East African Postal Service</strong><br>
On 1st July 1933 a Postal Union of the three East African territories came into operation and was further strengthened by the East African Customs and Postal Union formally introduced on 1st May 1935. Although the East African countries became independent sovereign states between 1961 and 1963, they continued cooperating closely with one another in a number of ways, not the least being their common postal services which culminated into the formation of East African Posts and Telecommunications Corporation (EAP&amp;T) under the Treaty for East African Cooperation which came into effect on 1st December 1967. In 1977, due to the break up of the East African Community, the Kenya Posts and Telecommunications Corporation (KP&amp;TC) was established. In the EAP&amp;TC and KP&amp;TC eras postal services were provided under a department of the larger Corporations.</p>
<p><strong>Establishment of PCK</strong><br>
Due to emerging market and economic trends and spearheaded by the Universal Postal Union (UPU), efforts to separate postal services from those of Telecommunications were initiated in the late 1980s. They bore fruit in Kenya and in July 1999, the KP&amp;TC was split to create, among others, the Postal Corporation of Kenya (PCK) whose mandate under the Postal Corporation Act of 1998 is to provide and operate postal services, postal financial services, and perform other functions and duties as the minister of communications may assign.</p>
<p>In pursuit of this mandate, PCK operates a network of 31 Head Post offices 472 departmental postal outlets and 204 postal agencies (sub-post offices). The population served per post office is 55,630. This is against a universal standard of 6,000 persons per post office. This network is served by a staff complement of 4,114 as at June 2009. Among the products and services of PCK are; letter post, parcels, Expedited Mail Services (EMS), Philately, Postal Financial Services (Money Orders and Postal Orders), Agency Services (Third party payments and receipts) and technology based money transfer services. Those planned for introduction are postal savings<br>
service, postal giro services and hybrid mail.</p>
